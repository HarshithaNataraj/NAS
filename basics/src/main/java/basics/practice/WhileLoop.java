package basics.practice;

public class WhileLoop {

	public static void main(String[] args) {
//		while loop syntax: while loop first check the condition and then execute the statement
//		initialization
//		while(condition) {
//			statements;
//			increment/decrement
//		}
		
		
		int i=0;
		while(i<=5) {
			System.out.println("hello");
			i++;
		}
		
		
//	do while : it will first execute the statement and then check the condition at
  
		int j=0;
		do {
			System.out.println("hello");
		}while(j<0);
	 
		

	}

}
